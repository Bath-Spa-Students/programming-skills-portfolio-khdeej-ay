# Write a Python program that uses a for loop to print the numbers from 1 to 10.

# initialize the iterator variable to 0
i = 0 
# while loop to loop through the numbers (it runs only till i<10)
while i < 10: 
    # augmented addition (+1 because we want numbers 1-10)
    i += 1
    # print is after the addition because we don't need the initial 0 to print
    print(i)
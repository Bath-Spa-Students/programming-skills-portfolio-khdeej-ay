'''Write a python code that uses three variables x, y, and z with respective 
values 10,20 and 30. Add all the variables and then store the result in 
variable a. Print the value of a. '''

# store 10 20 and 30 in x y and z respectively
x = 10
y = 20
z = 30 
# add x y and z and store their sum in variable a

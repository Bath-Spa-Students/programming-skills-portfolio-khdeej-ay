# Write a Python program to iterate through both the keys and values of a dictionary and print them

# random dictionary about my favourite numbers
fav_numbers = {
    "seven" : 7,
    "two" : 2, 
    "three" : 3, 
    "six" : 6
}

# for loop to iterate through keys in the dictionary
for key in fav_numbers: 
    print (key)

# for loop to iterate through values in the dictionary
for value in fav_numbers: 
    print (fav_numbers[value])
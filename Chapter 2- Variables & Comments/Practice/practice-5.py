'''Write a python program that stores fruit names in a list named fruits. 
Unpack the list into three variables x, y and z and then print the values 
of each variable.'''

# store fruit names in a list
fruits = ["peach", "grapes", "cherries"]
# store list indexes in separate variables (x y and z)
x = fruits[0]
y = fruits[1]
z = fruits[2]
# print each variable in separate print statements
print(x)
print(y)
print(z)